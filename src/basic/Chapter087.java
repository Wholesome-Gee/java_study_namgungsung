abstract class Unit {
  int x, y;
  // 모든 유닛은 이동해야 하지만 방식은 다름 -> 추상 메서드 [00:02:24]
  abstract void move(int x, int y); 
  void stop() { /* 현재 위치에 정지 */ }
}

class Marine extends Unit { // 보병
  void move(int x, int y) {
  	System.out.println("Marine[x=" + x + ", y=" + y + "] 이동");
  }
  void stimPack() { /* 스팀팩 사용 */ }
}

class Tank extends Unit { // 탱크
  void move(int x, int y) {
  	System.out.println("Tank[x=" + x + ", y=" + y + "] 이동");
  }
  void changeMode() { /* 공격 모드 변환 */ }
}

class Dropship extends Unit { // 수송선
  void move(int x, int y) {
  	System.out.println("Dropship[x=" + x + ", y=" + y + "] 이동");
  }
  void load() { /* 유닛 태우기 */ }
}

public class Chapter087 {
  public static void main(String[] args) {
  	// 1. 객체 배열 생성 및 초기화 (00:13:42)
  	Unit[] group = { new Marine(), new Tank(), new Dropship() };

  	// 2. 모든 유닛을 (100, 200) 좌표로 이동 (00:15:33)
  	for (int i = 0; i < group.length; i++) {
  	  // Unit 타입 리모컨으로 move() 호출 -> 실제 객체의 move()가 실행됨 [00:16:30]
  	  group[i].move(100, 200); 
  	}
  }
}

