/**
* [연산자 핵심 용어 정리]
	1. 연산자 (Operator) 
    - 연산을 수행하는 기호 (예: +, -, *, /, = 등)
	2. 피연산자 (Operand) 
    - 연산의 대상이 되는 값이나 변수 (예: 3 + 5 에서 '3'과 '5')
	3. 식 (Expression) 
    - 연산자와 피연산자를 조합하여 계산하고자 하는 문장
*/

public class Chapter023 {
  public static void main(String[] args) {
  	// 연산의 흐름 이해하기
  	int x = 5;
  	int y = 4 * x + 3;  
  	
  	System.out.println("y의 값: " + y); // 23 출력
  	
  	System.out.println(4 * 5 + 3); 
  }
}

