package basic; // 14. 리터럴의 타입과 접미사

/**
* [리터럴 타입 및 접미사]
	1. 정수형 리터럴과 접미사
    - int 타입: 접미사가 없으면 int입니다.
    - long 타입: 숫자 뒤에 'L'을 붙입니다. 
    - byte, short: 별도의 접미사가 없습니다.
	2. 실수형 리터럴과 접미사
    - double 타입: 접미사가 없으면 double입니다.
    - float 타입: 숫자 뒤에 'f'를 붙입니다.
	3. 진법 표현 (접두사)
    - 2진수: 숫자 앞에 '0b' (예: 0b10 -> 10진수로 2)
    - 8진수: 숫자 앞에 '0' (예: 010 -> 10진수로 8)
    - 16진수: 숫자 앞에 '0x' (예: 0x10 -> 10진수로 16)
	4. 변수와 리터럴의 타입 일치 규칙
    - 원칙: 변수의 타입과 리터럴의 타입은 일치해야 합니다.
    - 변수의 타입이 리터럴보다 더 넓은 범위이면 상관없습니다.
    - 변수의 타입이 리터럴보다 더 좁은 범위이면 에러가 발생합니다.
*/

public class Chapter014 {
  public static void main(String[] args) {
  	// 20억이 넘는 큰 숫자는 반드시 L을 붙여야 합니다.
  	long l = 10_000_000_000L; 
  	
  	// 3.14에 f를 안 붙이면 double로 인식되어 에러가 납니다.
  	float f = 3.14f; 
  	
  	// 변수의 타입(double)이 리터럴(float)보다 더 넓은 범위여서 에러가 발생하지 않습니다.
  	double d = 3.14f; 
  	
  	// 변수의 타입(float)이 리터럴(double)보다 더 좁은 범위여서 에러가 발생합니다.
  	// float f2 = 3.14d;
  }
}

