/**
* [핵심 요약]
	1. 단일 상속(Single Inheritance) (00:00:08)
		- 자바는 오직 하나의 부모로부터만 상속받을 수 있습니다. [00:00:14]
		- 다중 상속 시 발생할 수 있는 '메서드 이름 충돌' 문제를 원천 차단하기 위함입니다. [00:02:25]
		- 비중이 높은 클래스는 상속으로, 나머지는 **포함(HAS-A)** 관계로 처리하여 다중 상속의 효과를 낼 수 있습니다. [00:04:03]
	2. Object 클래스 - 모든 클래스의 조상 (00:05:33)
		- 조상이 없는 클래스는 컴파일러가 자동으로 `extends Object`를 추가합니다. [00:06:22]
		- 따라서 자바의 모든 클래스는 Object에 정의된 11개의 메서드(toString, equals 등)를 상속받습니다. [00:07:05]
	3. toString() 메서드 (00:08:20)
		- 객체의 정보를 문자열로 반환합니다. (기본값: 클래스이름@해쉬코드) [00:10:20]
		- `System.out.println(obj);`는 내부적으로 `obj.toString();`을 호출합니다. [00:11:55]
*/

class Tv {
  boolean power;
  void power() { power = !power; }
}

class Dvd {
  void play() { System.out.println("DVD를 재생합니다."); }
}

// 자바는 다중 상속이 안 되므로 하나는 상속, 하나는 포함으로 구성 [00:04:32]
class TvDvd extends Tv { // Tv는 상속
  Dvd dvd = new Dvd();   // Dvd는 포함
  
  void play() {
  	dvd.play(); // 포함된 객체의 메서드를 호출하여 사용 [00:05:08]
  }
}

public class Chapter072 {
  public static void main(String[] args) {
  	TvDvd td = new TvDvd();
  	
  	// 1. 상속과 포함의 조화
  	td.power(); // 조상 Tv의 메서드
  	td.play();  // 포함된 Dvd의 메서드 호출
  	
  	// 2. Object 클래스의 toString() 사용 [00:09:47]
  	// TvDvd 클래스에 toString을 만들지 않았어도 호출 가능!
  	System.out.println(td.toString()); 
  	System.out.println(td); // 위와 동일한 결과 (자동 호출) [00:12:09]
  }
}

