// 추상 클래스 정의 [00:02:25]
abstract class Player {
  boolean pause;
  int currentPos;

  Player() {
  	pause = false;
  	currentPos = 0;
  }

  // 추상 메서드: 선언부만 있고 몸통이 없음 [00:02:32]
  abstract void play(int pos);
  abstract void stop();

  // 일반 메서드도 포함 가능
  void play() {
  	play(currentPos); // 추상 메서드를 호출할 수 있음! [00:16:11]
  }
}

// 추상 클래스를 상속받아 완성한 클래스 [00:06:20]
class AudioPlayer extends Player {
  void play(int pos) { 
  	System.out.println(pos + "위치부터 재생을 시작합니다."); 
  }
  void stop() { 
  	System.out.println("재생을 멈춥니다."); 
  }
}

// 일부 추상 메서드만 구현한 경우, 여전히 추상 클래스여야 함 [00:11:17]
abstract class AbstractPlayer extends Player {
  void play(int pos) { /* 일부만 구현 */ }
  // stop()을 구현하지 않았으므로 abstract 클래스여야 에러가 안 남 [00:12:41]
}

public class Chapter086 {
  public static void main(String[] args) {
  	// Player p = new Player(); // 에러! 추상 클래스는 객체 생성 불가 [00:05:14]
  	
  	AudioPlayer ap = new AudioPlayer(); // 완성된 클래스는 생성 가능 [00:07:23]
  	
  	// 다형성 활용 가능 [00:07:56]
  	Player p = new AudioPlayer(); 
  	p.play(100); // 실제 구현된 AudioPlayer의 메서드가 호출됨 [00:28:16]
  	p.stop();
  }
}

