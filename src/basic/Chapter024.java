package basic; // 24. 연산자의 우선순위와 결합규칙

/**
* [연산자 우선순위 핵심 3법칙]
	1. 산술 > 비교 > 논리 > 대입 
    - 계산(+,-)을 먼저 하고, 비교(>, <)를 한 뒤, 논리(&&, ||)를 따지고, 마지막에 값을 저장(=)합니다.
	2. 단항 > 이항 > 삼항 
    - 피연산자가 1개인 것(예: 부호 -)이 2개(예: +)나 3개인 것보다 먼저 계산됩니다.
	3. 결합 규칙: 대부분 왼쪽 → 오른쪽 
    - 예외: 단항 연산자와 대입 연산자(=)만 오른쪽 → 왼쪽으로 진행됩니다.
*/

public class Chapter024 {
  public static void main(String[] args) {
  	// 상식적인 우선순위 예시
  	int result = 5 + 3 * 4; // 17
  	
  	// 내가 원하는 순서가 있다면? -> 괄호() 사용!
  	int result2 = (5 + 3) * 4; // 32

  	// 대입 연산자의 결합 규칙 (우 → 좌)
  	int x, y;
  	x = y = 3; // 1. y = 3 이 먼저, 그 후 x = y 실행
  	
  	// 논리 연산 예시
  	boolean check = x > 0 && x < 10; // x가 0보다 크고 10보다 작다 (비교를 먼저 하고 논리 && 수행) 
  }
}

