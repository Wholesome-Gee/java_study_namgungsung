package basic; // 59. 메서드란? 메서드의 선언부와 구현부

/**
* [핵심 요약]
	1. 메서드(Method)란? 
		- 특정 작업을 수행하는 일련의 문장들을 하나로 묶은 것입니다.
	2. 메서드 사용의 장점 
		- 코드의 중복 제거: 반복되는 코드를 하나로 묶어 관리할 수 있습니다.
		- 유지보수 용이: 수정 사항이 생기면 메서드 내부만 고치면 됩니다.
		- 재사용성 및 간결성: 코드가 짧아지고 이해하기 쉬워집니다.
	3. 메서드의 구조
		- 선언부(Header): 반환타입, 메서드이름, 매개변수 목록으로 구성됩니다.  
		- 구현부(Body): 메서드가 호출되었을 때 수행할 코드를 작성합니다. ({ } 블럭) [00:13:27]
		- int add(int a, int b) { return a + b; }
*/

public class Chapter059 {
	// 메서드 구현부
  static int add(int a, int b) { 
  	int result = a + b; // result는 지역변수(lv)
  	return result;
  }
  // 메서드 호출 
  public static void main(String[] args) {
  	int result = add(3, 5); 
  	System.out.println(result); // 8
  }
}

/**
 * [Gemini의 꼼꼼한 보충수업]
	1. 매개변수(Parameter)와 반환값(Return value)
		- 입력(매개변수)은 개수 제한이 없다.
			출력(반환값)은 최대 1개만 가능.
		- 결과값이 없을 때는 반환타입에 'void'를 적어줍니다.
	2. 지역변수(lv)와 매개변수의 특징
		- 메서드 내부에 선언된 변수와 매개변수는 모두 지역변수입니다.
		- 이들은 메서드가 종료되면 자동으로 소멸되므로, 다른 메서드에서 같은 이름의 변수를 사용해도 충돌하지 않습니다.
	4. 작성 원칙
		- 하나의 메서드는 '하나의 기능'만 
 */