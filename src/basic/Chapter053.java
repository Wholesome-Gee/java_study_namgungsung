package basic; // 53. 한파일 여러 클래스 작성하기

/**
* [핵심 요약]
	1. 소스파일과 클래스의 일대일 대응
		보통 하나의 소스파일(.java)에는 하나의 클래스만 작성하는 것이 원칙입니다.
	2. 소스파일 이름 결정 규칙 
		public 클래스가 있는 경우: 반드시 소스파일 이름은 public 클래스 이름과 일치해야 합니다. 
		public 클래스가 없는 경우: 파일 안의 어떤 클래스 이름으로 지어도 상관없습니다.
	3. 작성 시 주의사항
		하나의 소스파일에는 오직 하나의 public 클래스만 존재할 수 있습니다. 
		대소문자를 엄격하게 구분하므로 대소문자까지 정확히 일치해야 합니다.
*/

public class Chapter053 {
  public static void main(String[] args) {
  	// 이 소스파일의 이름이 Chapter053.java인 이유는
  	// public class의 이름이 Chapter053이기 때문입니다. 
  	System.out.println("한 파일에 여러 클래스 작성 시 규칙 학습");
  }
}
