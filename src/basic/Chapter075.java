/**
* [핵심 요약]
	1. 패키지(Package) (00:00:06)
		- 서로 관련된 클래스들의 묶음입니다. [00:00:10]
		- 물리적으로는 하나의 디렉토리(폴더)이며, 클래스의 실제 이름(Full Name)은 패키지명을 포함합니다. [00:01:26]
		- 예: `String` 클래스의 진짜 이름은 `java.lang.String`입니다. [00:01:37]
	2. 패키지 선언 (00:04:33)
		- 소스 파일의 첫 번째 문장(주석 제외)으로 딱 한 번 선언합니다. [00:04:42]
		- 패키지 선언이 없으면 '이름 없는 패키지(default package)'에 속하게 됩니다. [00:05:30]
	3. 클래스 패스(Classpath) (00:13:43)
		- 클래스 파일(`.class`)을 찾기 위한 경로를 OS에 알려주는 설정입니다. [00:14:03]
		- 환경 변수 `CLASSPATH`에 패키지의 루트 디렉토리를 등록하여 사용합니다. [00:14:52]
*/

public class Chapter075 {
  public static void main(String[] args) {
  	// 우리가 사용하는 클래스들이 어떤 패키지에 있는지 확인해봅시다.
  	System.out.println("현재 클래스의 패키지 포함 이름: " + Chapter075.class.getName());
  	
  	// java.lang 패키지는 너무 자주 쓰여서 자동으로 포함(import)됩니다. [00:08:01]
  	String str = "Hello, Package!"; 
  	System.out.println("String 클래스의 패키지: java.lang.String");
  }
}

