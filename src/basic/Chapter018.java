package basic; // 18. 기본형의 종류와 범위

/**
* [기본형 8개 타입 완전 정복]
	1. 논리형, 문자형
    - boolean(1 byte): true, false만 저장 가능.
    - char(2 byte): 유니코드(2바이트 문자체계)를 사용하며, 한 글자 저장.
	2. 정수형 
    - byte(1 byte) < short(2 byte) < int(4 byte) < long(8 byte)
    - int: 기본 타입(Default). 약 ±20억까지 가능. 
    - long: 아주 큰 숫자(약 ±800경)를 다룰 때 사용. 
	3. 실수형
    - float(4 byte): 정밀도 7자리.
    - double(8 byte): 기본 타입. 정밀도 15자리로 훨씬 정확함.
	4. 데이터 표현 범위 공식
    - n 비트로 표현할 수 있는 값의 개수: 2의n승 개
    - 부호 있는 정수의 범위: -2의(n-1)승 ~ 2의(n-1)승 - 1
*/

public class Chapter018 {
  public static void main(String[] args) {
  	// int는 약 20억까지 가능하므로, 그 이상은 long을 써야 함
  	int i = 2_147_483_647; 
  	long l = 922_3372_0368_5477_5807L; // 20억 넘으면 반드시 L 접미사 필요

  	// 실수는 정확도가 중요하므로 보통 double을 사용
  	double d = 3.141592653589793;
  	float f = 3.141592653589793f;
  }
}

