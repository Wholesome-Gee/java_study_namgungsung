package basic; // 11. 변수(Variable)의 개념과 선언

/**
* [변수 기초 가이드]
	1. 변수란 무엇인가? 
    - 하나의 값을 저장할 수 있는 메모리 공간.
    - 메모리주소대신 사람이 이해하기 쉬운 이름(Age, X 등)을 붙인 것.
    - 변수에는 오직 하나의 값만 저장할 수 있으며, 새 값을 넣으면 기존 값은 지워진다.
	2. 변수의 선언과 타입
    - 형식: int age;  String name;
	3. 변수에 값 저장하기 (대입 연산자) 
    - age = 25; 에서 '='는 "같다"가 아니라 "오른쪽 값을 왼쪽에 저장하라"는 뜻.
    - 이를 '대입 연산자'라고 부름.
	4. 변수의 초기화 
    - 변수를 선언하고 처음으로 값을 저장하는 것을 말함.
    - 지역 변수는 사용하기 전에 반드시 초기화.
    - int x = 5, y = 10; 처럼 콤마를 이용해 한 줄에 여러 개를 선언할 수 있음.
*/

public class Chapter011 {
  public static void main(String[] args) {
  	// [00:10:45] 변수를 사용하면 값이 바뀌어도 한 곳만 수정하면 됩니다.
  	int x = 6;
  	int y = 3;

  	System.out.println(x + y); // 9
  	System.out.println(x - y); // 3
  	System.out.println(x * y); // 18
  	System.out.println(x / y); // 2
  }
}

