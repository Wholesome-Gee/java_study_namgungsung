package basic; // 94. 내부 클래스의 종류와 특징

/**
* [핵심 요약]
	1. 내부 클래스란? 
		- 클래스 내부에 선언된 클래스. 
		- 두 클래스가 서로 아주 긴밀한 관계에 있을 때 사용.
	
	2. 장점
		- 내부 클래스 외부 클래스의 멤버에 직접 접근가능.
		- 코드의 복잡성을 줄이고 캡슐화를 강화.
		
	3. 종류
		- 인스턴스 내부 클래스 (인스턴스변수와 유사)
		- 스태틱 내부 클래스 (클래스변수와 유사)
		- 지역 내부 클래스 (지역변수와 유사)
		- 익명 클래스: 클래스 선언과 객체 생성을 동시에 하는 이름 없는 클래스
*/

class Outer {
  private int iv = 10;
  private static int cv = 20;

  // 인스턴스 내부 클래스 (인스턴스 변수와 유사)
  class InstanceInnerClass {
  	int iiv = iv; // 외부 클래스의 private 멤버에 직접 접근.
  }

  // 스태틱 내부 클래스 (클래스 변수와 유사) 
  static class StaticInnerClass  {
  	// int siv = iv; → 스태틱 클래스는 외부의 인스턴스 멤버에 접근 불가.
  	int scv = cv;
  }

  // 지역 내부 클래스 (지역 변수와 유사, 메서드 내에서만 사용)
  void method() {
  	class LocalInnerClass {
  	  int liv = iv;
  	  int lcv = cv;
  	}
  }
}

public class Chapter094 {
  public static void main(String[] args) {
  	// 내부 클래스 선언 위치 예시용 코드
  	System.out.println("내부 클래스는 외부 멤버에 대한 접근성이 뛰어납니다.");
  }
}

