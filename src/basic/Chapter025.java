package basic; // 25. 증감 연산자와 부호 연산자

/**
* [증감 연산자 핵심 요약]
	1. 증감 연산자 (++, --)
    - 증가 연산자(++): 피연산자의 값을 1 증가시킵니다.
    - 감소 연산자(--): 피연산자의 값을 1 감소시킵니다.
	2. 전위형 vs 후위형 
    - 전위형(++i): 값이 먼저 증가하고, 그 후에 다른 연산에 사용됩니다.
    - 후위형(i++): 다른 연산에 먼저 사용되고, 그 후에 값이 증가합니다.
	3. 독립적으로 쓰일 때
    - `++i;`나 `i++;`만 단독으로 쓰인다면 결과는 똑같이 1 증가입니다. 차이가 없어요!
*/

public class Chapter025 {
  public static void main(String[] args) {
  	// 증감 연산자 예시
  	int i = 5, j = 0;

  	// 후위형 '나중에 증가'
  	j = i++; 
  	System.out.println("i: " + i + " / j: " + j); // i: 6 / j: 5

  	// 전위형 '먼저 증가'
  	j = ++i; 
  	System.out.println("i: " + i + " / j: " + j); // i: 7 / j: 7

  	// 3. 부호 연산자 (-)
  	int k = -10;
  	k = -k; // 부호를 반대로 바꿈
  	System.out.println("k의 부호 변경 결과: " + k); // 10
  }
}