package basic; // 21. 정수형의 오버플로우

/**
* [오버플로우]
	1. 오버플로우란?
    - 타입이 표현할 수 있는 값의 범위를 넘어서는 것을 말합니다.
    - 마치 4자리 계수기에서 9999 다음이 0000이 되는 것과 같습니다.
	2. 정수형 오버플로우의 규칙 
    - 최대값 + 1 -> 최소값 
    - 최소값 - 1 -> 최대값 
	3. 부호 유무에 따른 차이 
    - 부호 없는 정수(char): 0 ~ 65535 (범위를 넘으면 다시 0으로)
    - 부호 있는 정수(short): -32768 ~ 32767 (최대값 32767을 넘으면 -32768로)
*/

public class Chapter021 {
  public static void main(String[] args) {
  	// short(= 2byte = 16bit) 정수 예시
  	short sMax = 32767; 
  	short sMin = -32768;

  	// 최대값에 1을 더하면? 
  	System.out.println((short)(sMax + 1)); // -32768 (최소값)

  	// 최소값에서 1을 빼면?
  	System.out.println((short)(sMin - 1)); // 32767 (최대값)

  	// 부호 없는 정수(char = 2byte = 16bit) 예시
  	char cMin = 0;
  	System.out.println((int)--cMin); // 결과: 65535 (최대값) [00:05:51]
  }
}

