package basic; // 13. 상수와 리터럴

/**
* [상수와 리터럴]
	1. 상수(Constant)란?
    - 값을 단 한 번만 저장할 수 있는 공간입니다.
    - 변수 앞에 'final' 키워드를 붙여서 선언합니다.
    - 초기화 이후에 값을 변경하려고 하면 컴파일 에러가 발생합니다.
	2. 리터럴(Literal)이란?
    - 그 자체로 값을 의미하는 것입니다. (예: 100, 'A', "abc")
	3. 상수의 초기화
    - 상수는 사용하기 전에 반드시 초기화해야 합니다.
    - 선언과 동시에 초기화하는 것이 가장 좋은 습관입니다.
*/

public class Chapter013 {
  public static void main(String[] args) {
  	int score = 100; 
  	score = 200; // 변수는 값 변경 가능

  	final int MAX_SCORE = 100; 
  	// MAX_SCORE = 200; // 에러 발생! 상수는 변경 불가

  	System.out.println(score);
  	System.out.println(MAX_SCORE);
  }
}

