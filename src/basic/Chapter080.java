/**
* [핵심 요약]
	1. 다형성이란? (00:01:11)
		- 여러 가지 형태를 가질 수 있는 능력을 의미합니다.
		- 자바에서는 조상 타입의 참조 변수로 자손 타입의 객체를 다루는 것을 말합니다. [00:01:35]
	2. 타입 불일치 허용 (00:03:17)
		- 원래는 참조 변수와 객체의 타입이 일치해야 하지만, 상속 관계에서는 조상 타입 변수에 자손 객체를 담을 수 있습니다. [00:05:42]
	3. 핵심 규칙 (00:15:24)
		- 조상 타입 참조 변수 = 자손 객체 (OK!) [00:05:02]
		- 자손 타입 참조 변수 = 조상 객체 (에러!) [00:10:33]
*/

class Tv {
  boolean power;
  int channel;
  void power() { power = !power; }
  void channelUp() { ++channel; }
  void channelDown() { --channel; }
}

class SmartTv extends Tv {
  String text;
  void caption() { /* 자막 보여주는 기능 */ }
}

public class Chapter080 {
  public static void main(String[] args) {
  	// 1. 일반적인 경우: 타입 일치 [00:02:55]
  	SmartTv s = new SmartTv(); // 멤버 7개 사용 가능
  	
  	// 2. 다형성의 경우: 타입 불일치 (조상 변수 = 자손 객체) [00:03:40]
  	Tv t = new SmartTv(); 
  	// 실제 객체는 SmartTv(멤버 7개)지만, 
  	// 리모컨(참조 변수 t)이 Tv 타입이라 버튼이 5개뿐입니다.
  	// 따라서 t.caption() 같은 SmartTv 전용 기능은 사용 불가합니다. [00:08:24]
  	
  	// 3. 허용되지 않는 경우: 자손 변수 = 조상 객체 [00:10:33]
  	// SmartTv ss = new Tv(); // 에러 발생! [00:15:00]
  	// 리모컨 버튼(7개)이 실제 제품 기능(5개)보다 많으면 사고가 나기 때문입니다. [00:12:42]
  }
}

