public class Chapter037 {
  public static void main(String[] args) {
  	// 구구단 출력 예제
  	for (int i = 2; i <= 9; i++) { // 2단부터 9단까지 (바깥쪽)
  		for (int j = 1; j <= 9; j++) { // 1부터 9까지 곱하기 (안쪽)
  			System.out.printf("%d * %d = %d%n", i, j, i * j);
  		}
  		System.out.println(); // 한 단이 끝나면 줄바꿈
  	}

  	// 직각삼각형 별 찍기 예제
  	for (int i = 1; i <= 5; i++) { // 줄 수 (1행~5행)
  		for (int j = 1; j <= i; j++) { // 별의 개수 (i값에 맞춰 늘어남!)
  			System.out.print("*");
  		}
  		System.out.println(); // 줄바꿈
  	}
  }
}

