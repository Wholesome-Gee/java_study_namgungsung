package basic; // 78. 접근 제어자

/**
* [핵심 요약]
	1. 접근 제어자란? (00:00:09)
		- 멤버나 클래스에 사용되며, 외부로부터의 접근을 제한하는 역할을 합니다.
		- 4가지 중 하나만 선택해서 사용할 수 있습니다. [00:00:13]
	2. 접근 범위 (넓은 순서) (00:02:22)
		- public: 접근 제한이 전혀 없습니다. [00:01:01]
		- protected: 같은 패키지 + 다른 패키지의 자손 클래스에서 접근 가능합니다. [00:01:40]
		- (default): 같은 패키지 내에서만 접근 가능합니다. (제어자를 붙이지 않은 상태) [00:01:31]
		- private: 같은 클래스 내에서만 접근 가능합니다. [00:01:26]
*/

public class Chapter078 { // 클래스 앞에는 public 또는 (default)만 가능 [00:02:36]
  private   int pri = 10; // 같은 클래스 내에서만 OK [00:06:02]
  int dft = 20;           // 같은 패키지 내에서만 OK [00:07:13]
  protected int pro = 30; // 같은 패키지 + 자손 클래스 OK [00:12:22]
  public    int pub = 40; // 누구나 OK [00:13:12]

  public void printMembers() {
  	// 같은 클래스 내부이므로 모든 제어자에 접근 가능합니다. [00:05:03]
  	System.out.println(pri);
  	System.out.println(dft);
  	System.out.println(pro);
  	System.out.println(pub);
  }
}

/**
 * [Gemini의 꼼꼼한 보충수업]
	1. 클래스에 붙일 수 있는 제어자 (00:02:31)
		- 클래스 앞에는 `public`과 `(default)`만 붙일 수 있습니다. 
		- 하나의 소스 파일에는 `public` 클래스가 단 하나만 존재해야 하며, 그 이름은 파일명과 일치해야 합니다. [00:09:57]
	2. 상속과 protected (00:12:09)
		- 패키지가 다르더라도 상속 관계에 있다면 `protected` 멤버에 접근할 수 있습니다. 이것이 `default`와의 결정적인 차이입니다. [00:18:00]
	3. 가시성 가이드 (00:19:52)
		- private: "이 파일(클래스) 안에서만 쓸 거야!" [00:20:23]
		- default: "우리 폴더(패키지) 사람들끼리만 쓰자!" [00:20:31]
		- protected: "우리 폴더 사람들이랑, 나를 따라 하는 자손들까지는 허용해 줄게!" [00:21:06]
 */

